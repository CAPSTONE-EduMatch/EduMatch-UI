/**
 * Get a random image URL from an array
 * @param urls Array of image URLs
 * @returns Random URL from the array
 */
export function getRandomFromArray(urls: string[]): string {
	return urls[Math.floor(Math.random() * urls.length)];
}

/**
 * Get an image URL by index (cycles through array)
 * Useful for consistent assignment based on entity ID
 * @param urls Array of image URLs
 * @param index Index (will be modulo'd to array length)
 * @returns URL at the cycled index
 */
export function getByIndex(urls: string[], index: number): string {
	return urls[index % urls.length];
}

/**
 * Get multiple unique random images from an array
 * @param urls Array of image URLs
 * @param count Number of images to get
 * @returns Array of unique URLs (or as many as available)
 */
export function getMultipleRandom(urls: string[], count: number): string[] {
	const shuffled = [...urls].sort(() => Math.random() - 0.5);
	return shuffled.slice(0, Math.min(count, urls.length));
}

